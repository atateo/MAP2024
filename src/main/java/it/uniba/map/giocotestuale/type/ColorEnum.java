package it.uniba.map.giocotestuale.type;

/**
 * Enum che rappresenta i colori che saranno implementati nel gioco.
 */
public enum ColorEnum {
    /**
     * Valore neutro (non colorato).
     */
    NEUTRAL,
    /**
     * Valore del colore rosso.
     */
    RED,
    /**
     * Valore del colore giallo.
     */
    YELLOW,
    /**
     * Valore del colore blu.
     */
    BLUE,
    /**
     * Valore del colore viola.
     */
    PURPLE,
    /**
     * Valore del colore verde.
     */
    GREEN,
    /**
     * Valore del colore marrone.
     */
    BROWN
}
